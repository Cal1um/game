import Rock from "/rock.js";
import Enemy from "/enemy.js";
import Upgrade from "/upgrades.js";
import Boss from "/boss.js";

export function buildLevel(game, level) {
    let tiles = [];
    level.forEach((row, rowIndex) => {
        row.forEach((tile, tileIndex) => {
            //walls named rock
            if(tile === 1) {

                let position = {
                    x: 90 * tileIndex,
                    y: 0 + 90 * rowIndex
                }
                tiles.push(new Rock(game, position));
            }
            //enemy
            if(tile === 2){

                let position = {
                    x: 90 * tileIndex,
                    y: 0 + 90 * rowIndex
                }
                tiles.push(new Enemy(game, position));
            }
            //upgrades
            if(tile === 3){

                let position = {
                    x: 90 * tileIndex + 30,
                    y: 0 + 90 * rowIndex + 30
                }
                tiles.push(new Upgrade(game, position));
            }
            //boss
            if(tile === 4){

                let position = {
                    x: 90 * tileIndex,
                    y: 0 + 90 * rowIndex
                }
                tiles.push(new Boss(game, position));
            }
        })
        
    });

    return tiles
    
}

